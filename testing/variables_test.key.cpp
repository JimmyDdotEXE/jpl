#include <iostream>
#include <string>


int main(int argc, char *argv[]){
	//JPL variables are strictly typed.
	//Variables can have type byte, short, int, long, decimal, bool, char, or string.




	//Variable declarations need a type and a name.
	int x = 0;	//variables auto-initialize to a base value at declaration. x has value 0 here

	int y = 5;	//variables can be initialized at declaration. y has value 5 here




	//Variables that have previously been declared can be referenced by name.
	x = y + 1;	//x now has value 6
}
