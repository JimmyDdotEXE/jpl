#include <iostream>
#include <string>


int main(int argc, char *argv[]){
	//JPL offers a print statement to print values to the screen.




	//String values can be printed.
	std::cout << "Turtles are cool" << std::endl;




	//Char values can be printed.
	std::cout << 'a' << std::endl;




	//Num values can be printed.
	std::cout << 5 + 10 << std::endl;




	//Cond values can be printed.
	std::cout << (true ? "True" : "False") << std::endl;
}
